from __future__ import (
    unicode_literals,
    print_function,
    absolute_import,
    division,
    )

from dbsuite.termsize import terminal_size

def test_for_sane_size():
    cols, rows = terminal_size()
    print('Terminal size received:', cols, 'x', rows)
    assert cols > 0
    assert rows > 0
