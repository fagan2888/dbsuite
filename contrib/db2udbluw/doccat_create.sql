	ON DOCDATA.ROUTINES (ROUTINESCHEMA, SPECIFICNAME)
	ALLOW REVERSE SCANS!

ALTER TABLE DOCDATA.ROUTINES
	ADD CONSTRAINT PK PRIMARY KEY (ROUTINESCHEMA, SPECIFICNAME)!

