	ON DOCDATA.INDEXES (INDSCHEMA, INDNAME)
	ALLOW REVERSE SCANS!

ALTER TABLE DOCDATA.INDEXES
	ADD CONSTRAINT PK PRIMARY KEY (INDSCHEMA, INDNAME)!

-------------------------------------------------------------------------------
-- Comments for ROUTINES
-------------------------------------------------------------------------------
